package business;

/**
 *
 * @author miles
 */
public class item {
    private int id;
    private String name;
    private double amount;
    private String cost;
    
    public item() {
    this.name = "";
    this.amount = 0;
    this.cost = "";
        
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
